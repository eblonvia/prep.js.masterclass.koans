import { expect } from 'chai';

describe('Introduction to Mocha and WallabyJS', () => {
  it('List all the falsy values in JavaScript', () => {
    let falsy = __;
    expect(falsy).to.not.be.ok;
    falsy = __;
    expect(falsy).to.not.be.ok;
    falsy = __;
    expect(falsy).to.not.be.ok;
    falsy = __;
    expect(falsy).to.not.be.ok;
    falsy = __;
    expect(falsy).to.not.be.ok;
  });
});
